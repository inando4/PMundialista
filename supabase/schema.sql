create extension if not exists pgcrypto;

do $$
begin
  create type public.match_status as enum ('scheduled', 'live', 'finished');
exception
  when duplicate_object then null;
end $$;

create table if not exists public.admins (
  email text primary key,
  created_at timestamptz not null default now()
);

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  display_name text not null,
  email text not null unique,
  has_paid boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.matches (
  id bigint generated by default as identity primary key,
  home_team text not null,
  away_team text not null,
  starts_at timestamptz not null,
  stage text not null default 'Fase de grupos',
  home_score integer,
  away_score integer,
  status public.match_status not null default 'scheduled',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint matches_non_negative_scores check (
    (home_score is null or home_score >= 0) and
    (away_score is null or away_score >= 0)
  ),
  constraint matches_finished_has_score check (
    status <> 'finished' or (home_score is not null and away_score is not null)
  )
);

create table if not exists public.predictions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  match_id bigint not null references public.matches(id) on delete cascade,
  predicted_home_score integer not null,
  predicted_away_score integer not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (user_id, match_id),
  constraint predictions_non_negative_scores check (
    predicted_home_score >= 0 and predicted_away_score >= 0
  )
);

create table if not exists public.scoring_rules (
  id text primary key default 'default',
  result_points integer not null default 1,
  exact_bonus_points integer not null default 3,
  updated_at timestamptz not null default now(),
  constraint scoring_rules_non_negative check (result_points >= 0 and exact_bonus_points >= 0)
);

create table if not exists public.change_history (
  id bigint generated by default as identity primary key,
  actor_id uuid references public.profiles(id) on delete set null,
  entity text not null,
  entity_id text not null,
  change_type text not null,
  before_data jsonb,
  after_data jsonb,
  created_at timestamptz not null default now()
);

insert into public.scoring_rules (id, result_points, exact_bonus_points)
values ('default', 1, 3)
on conflict (id) do nothing;

create or replace function public.touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, display_name, email)
  values (
    new.id,
    coalesce(nullif(new.raw_user_meta_data ->> 'display_name', ''), split_part(new.email, '@', 1)),
    new.email
  )
  on conflict (id) do nothing;

  return new;
end;
$$;

