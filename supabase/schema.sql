create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.admins
    where lower(email) = lower(coalesce(auth.jwt() ->> 'email', ''))
  );
$$;

create or replace function public.match_outcome(home_score integer, away_score integer)
returns text
language sql
immutable
as $$
  select case
    when home_score > away_score then 'home'
    when home_score < away_score then 'away'
    else 'draw'
  end;
$$;

create or replace view public.leaderboard as
select
  profiles.id as profile_id,
  profiles.display_name,
  profiles.email,
  profiles.has_paid,
  coalesce(sum(
    case
      when matches.status = 'finished'
        and predictions.id is not null
        and public.match_outcome(predictions.predicted_home_score, predictions.predicted_away_score) =
            public.match_outcome(matches.home_score, matches.away_score)
      then rules.result_points
      else 0
    end +
    case
      when matches.status = 'finished'
        and predictions.predicted_home_score = matches.home_score
        and predictions.predicted_away_score = matches.away_score
      then rules.exact_bonus_points
      else 0
    end
  ), 0)::integer as points,
  count(*) filter (
    where matches.status = 'finished'
      and predictions.id is not null
      and public.match_outcome(predictions.predicted_home_score, predictions.predicted_away_score) =
          public.match_outcome(matches.home_score, matches.away_score)
  )::integer as correct_results,
  count(*) filter (
    where matches.status = 'finished'
      and predictions.predicted_home_score = matches.home_score
      and predictions.predicted_away_score = matches.away_score
  )::integer as exact_scores
from public.profiles
cross join public.scoring_rules rules
left join public.predictions on predictions.user_id = profiles.id
left join public.matches on matches.id = predictions.match_id
where rules.id = 'default'
group by profiles.id, profiles.display_name, profiles.email, profiles.has_paid
order by points desc, exact_scores desc, correct_results desc, profiles.display_name asc;

